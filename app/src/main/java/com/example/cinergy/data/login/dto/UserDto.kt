package com.example.cinergy.data.login.dto

import com.squareup.moshi.Json

data class UserDto(
    @Json(name = "id")
    val id: Long,
    @Json(name = "role_id")
    val roleId: Long,
    @Json(name = "location_id")
    val locationId: String?,
    @Json(name = "address1")
    val address1: String?,
    @Json(name = "address2")
    val address2: String?,
    @Json(name = "city")
    val city: String?,
    @Json(name = "zip")
    val zip: String?,
    @Json(name = "booking_token")
    val bookingToken: String?,
    @Json(name = "access_token")
    val accessToken: String?,
    val name: String,
    @Json(name = "first_name")
    val firstName: String?,
    @Json(name = "last_name")
    val lastName: String?,
    @Json(name = "email")
    val email: String?,
    @Json(name = "phone_code")
    val phoneCode: String,
    @Json(name = "phone")
    val phone: String?,
    @Json(name = "image")
    val image: String?,
    @Json(name = "gender")
    val gender: String?,
    @Json(name = "birthday")
    val birthday: String?,
    @Json(name = "cinema_id")
    val cinemaId: String?,
    @Json(name = "member_id")
    val memberId: String?,
    @Json(name = "username")
    val username: String,
    @Json(name = "encrypted_password")
    val encryptedPassword: Any?,
    @Json(name = "user_role")
    val userRole: Long,
    @Json(name = "gmail_login")
    val gmailLogin: String?,
    @Json(name = "fb_login")
    val fbLogin: String?,
    @Json(name = "opt_out1")
    val optOut1: Long,
    @Json(name = "opt_out2")
    val optOut2: Long,
    @Json(name = "ip_access")
    val ipAccess: Any?,
    @Json(name = "is_active")
    val isActive: Long,
    @Json(name = "created_at")
    val createdAt: String,
    @Json(name = "updated_at")
    val updatedAt: String,
    @Json(name = "deleted_at")
    val deletedAt: Any?,
    @Json(name = "location_name")
    val locationName: String,
    @Json(name = "member_level_name")
    val memberLevelName: String,
    @Json(name = "subscribed_member")
    val subscribedMember: Long,
)